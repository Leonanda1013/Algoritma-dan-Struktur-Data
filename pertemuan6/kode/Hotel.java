package pertemuan6.kode;

public class Hotel {
    public String nama, kota;
    public int harga;
    public Byte bintang;

    public Hotel(String nama, String kota, int harga, Byte bintang) {
        this.nama = nama;
        this.kota = kota;
        this.bintang = bintang;
        this.harga = harga;
    }
}